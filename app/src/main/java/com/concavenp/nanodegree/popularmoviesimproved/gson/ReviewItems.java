package com.concavenp.nanodegree.popularmoviesimproved.gson;

import java.util.ArrayList;
import java.util.List;

/**
 * This is a GSON hand rolled class.
 * <p/>
 * Created by dave on 11/2/2015.
 */
@SuppressWarnings("unused")
public class ReviewItems {

    private final List<ReviewItem> results = new ArrayList<>();
    private int page;
    private int total_pages;
    private int total_results;

    public int getPage() {
        return page;
    }

    public List<ReviewItem> getResults() {
        return results;
    }

    public int getTotal_pages() {
        return total_pages;
    }

    public int getTotal_results() {
        return total_results;
    }

    public static class ReviewItem {

        private String id;
        private String author;
        private String content;
        private String url;

        public String getId() {
            return id;
        }

        public String getAuthor() {
            return author;
        }

        public String getContent() {
            return content;
        }

        public String getUrl() {
            return url;
        }
    }

}
